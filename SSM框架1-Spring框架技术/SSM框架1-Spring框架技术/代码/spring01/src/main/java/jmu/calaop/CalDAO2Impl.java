package jmu.calaop;

import org.springframework.stereotype.Repository;

@Repository("cal2")
public class CalDAO2Impl implements CalDAO2{
    public int add(int i, int j) {
        int result = i + j;
        return result;
    }

    public int sub(int i, int j) {
        int result = i - j;
        return result;
    }

    public int mul(int i, int j) {
        int result = i * j;
        return result;
    }

    public int div(int i, int j) {
        int result = i / j;
        return result;
    }
}
